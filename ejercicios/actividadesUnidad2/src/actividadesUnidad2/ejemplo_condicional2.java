package actividadesUnidad2;

public class ejemplo_condicional2 {

	public static void main(String[] args) {
		// sw + ctr + space
		int dia=3;
		switch (dia) {
		case 1:
			System.out.println ("Lunes");
			break;
		case 2:
			System.out.println ("Martes");
			break;
		case 3:
			System.out.println ("Miercoles");
			break;
			
			
			
			
			
			
			
		default:
			break;
		}

	}

}
